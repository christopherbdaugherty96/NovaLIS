def main() -> int:
    if not REPO_ROOT.exists():
        print(f"Repo root not found: {REPO_ROOT}")
        return 1

    notes = scan_notes()
    by_module, imports_by_file = _build_import_index(notes)
    test_pairs = _pair_tests_to_sources(notes, by_module)

    write_home(notes)
    write_user_paths(notes)
    write_repo_by_folder(notes)
    write_by_phase(notes)
    write_by_topic(notes)
    write_by_type(notes)
    write_code_by_layer(notes)
    write_code_modules(notes, by_module, imports_by_file, test_pairs)
    write_code_imports(notes, by_module, imports_by_file)
    write_test_map(notes, test_pairs)
    write_recent(notes)
    write_obsidian_config()

    doc_count = sum(1 for note in notes if note.kind == "doc")
    code_count = sum(1 for note in notes if note.kind == "code")
    print(
        f"Indexed {doc_count} docs and {code_count} code files. "
        f"MOCs -> {MOC_DIR}; config -> {VAULT_CONFIG_DIR}."
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
