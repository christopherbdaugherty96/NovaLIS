@dataclass
class NoteRef:
    path: Path
    rel: Path
    title: str
    category: str
    kind: str = "doc"
    phase: str | None = None
    topics: set[str] = field(default_factory=set)
    summary: str = ""


def _tracked_repo_paths() -> list[Path]:
    try:
        raw = subprocess.check_output(["git", "ls-files", "-z"], cwd=REPO_ROOT)
    except (OSError, subprocess.CalledProcessError):
        return []
    paths: list[Path] = []
    for chunk in raw.split(b"\0"):
        if not chunk:
            continue
        rel = chunk.decode("utf-8", errors="surrogateescape")
        path = REPO_ROOT / rel
        if path.exists() and path.is_file():
            paths.append(path)
    return paths


def _root_note_files() -> tuple[Path, ...]:
    return tuple(
        path
        for path in (
            REPO_ROOT / "README.md",
            REPO_ROOT / "REPO_MAP.md",
            REPO_ROOT / "CONTRIBUTING.md",
            REPO_ROOT / "SECURITY.md",
        )
        if path.exists()
    )


def _runtime_code_queries() -> list[str]:
    src_root = REPO_ROOT / "nova_backend" / "src"
    if not src_root.exists():
        return []
    excluded = {"governor", "governance", "__pycache__"}
    return [
        f"path:nova_backend/src/{entry.name}"
        for entry in sorted(src_root.iterdir(), key=lambda item: item.name.lower())
        if entry.is_dir() and entry.name not in excluded
    ]


def _iter_source_files() -> Iterable[Path]:
    seen: set[Path] = set()
    tracked = _tracked_repo_paths()
    if tracked:
        for path in tracked:
            try:
                rel_parts = path.relative_to(REPO_ROOT).parts
            except ValueError:
                continue
            if any(part in SKIP_DIR_NAMES for part in rel_parts):
                continue
            if path in seen:
                continue
            seen.add(path)
            yield path
    for path in _root_note_files():
        if path in seen:
            continue
        seen.add(path)
        yield path
    if MOC_DIR.exists():
        for path in MOC_DIR.glob("*.md"):
            if path in seen:
                continue
            seen.add(path)
            yield path


def _extract_phase(rel: Path) -> str | None:
    for part in rel.parts:
        match = PHASE_DIR_RE.match(part)
        if match:
            return f"Phase {match.group(1)}"
    match = PHASE_FILE_RE.search(rel.stem)
    if match:
        return f"Phase {match.group(1)}"
    return None


def _extract_topics(rel: Path, head: str) -> set[str]:
    rel_text = rel.as_posix()
    rel_lower = rel_text.lower()
    topics: set[str] = set()

    for path_prefix, hinted_topics in TOPIC_PATH_HINTS.items():
        if rel_text.startswith(path_prefix):
            topics.update(hinted_topics)

    heading_match = TITLE_HEADING_RE.search(head)
    heading_text = heading_match.group(1).lower() if heading_match else ""
    summary_text = _extract_summary(head, "doc" if rel.suffix.lower() in NOTE_SUFFIXES else "code").lower()
    haystack = f"{rel_lower} {heading_text} {summary_text}"
    for topic, keywords in TOPIC_KEYWORDS.items():
        if any(keyword in haystack for keyword in keywords):
            topics.add(topic)
    return topics


def _classify(rel: Path, phase: str | None) -> tuple[str, str]:
    parts = rel.parts
    parts_lower = [part.lower() for part in parts]
    suffix = rel.suffix.lower()
    if suffix in NOTE_SUFFIXES:
        kind = "doc"
    elif suffix in ASSET_SUFFIXES:
        kind = "asset"
    else:
        kind = "code"

    if "archive" in parts_lower or any(part.startswith("archive(") for part in parts_lower):
        return "archive", kind
    if "proofs" in parts_lower:
        return "proofs", kind
    if parts_lower[:2] == ["nova_backend", "tests"] or parts_lower[:2] == ["docs", "testing"] or parts_lower[:1] == ["verification"]:
        return "tests", kind
    if parts_lower[:1] == ["automations"] or parts_lower[:1] == ["scripts"] or parts_lower[:1] == [".github"]:
        return "scripts", kind
    if parts_lower[:2] == ["docs", "tools"]:
        return "scripts", kind
    if parts_lower[:2] == ["nova_backend", "static"] or parts_lower[:1] == ["nova-frontend-dashboard"]:
        return "frontend", kind
    if parts_lower[:1] == ["novalis-governance"]:
        return "governance", kind
    if parts_lower[:2] == ["nova_backend", "src"]:
        if len(parts_lower) > 2 and parts_lower[2] in {"governor", "governance"}:
            return "governance", kind
        return "runtime", kind
    if parts_lower[:1] == ["nova_workspace"]:
        return "runtime", kind
    if parts_lower[:1] == ["pull_requests"]:
        return "reference", kind
    if parts_lower[:1] == ["docs"]:
        if len(parts_lower) > 1 and parts_lower[1] in {"governance", "canonical", "canon"}:
            return "governance", kind
        if len(parts_lower) > 1 and parts_lower[1] in {"current_runtime", "runtime"}:
            return "runtime", kind
        if len(parts_lower) > 1 and parts_lower[1] == "reference":
            return "reference", kind
        if len(parts_lower) > 1 and parts_lower[1] == "proofs":
            return "proofs", kind
        if len(parts_lower) > 1 and parts_lower[1] == "design":
            if phase:
                return "phase", kind
            return "design", kind
        if phase:
            return "phase", kind
        return "reference", kind
    if phase:
        return "phase", kind
    if len(parts_lower) == 1 and suffix in NOTE_SUFFIXES:
        return "reference", kind
    return "reference", kind


def _extract_title(path: Path, head: str, kind: str) -> str:
    if kind == "doc":
        match = TITLE_HEADING_RE.search(head)
        if match:
            return match.group(1).strip()
    if kind == "asset":
        return path.name
    return path.stem.strip() or path.stem


def _extract_summary(head: str, kind: str, max_len: int = 140) -> str:
    if kind == "asset":
        return ""
    if kind == "code":
        for raw_line in head.splitlines():
            line = raw_line.strip()
            if not line or line.startswith("#!"):
                continue
            line = line.lstrip("#").lstrip("/").lstrip("*").strip(" \"'`")
            lowered = line.lower()
            if lowered.startswith("from __future__ import"):
                continue
            if lowered.startswith("from ") or lowered.startswith("import "):
                continue
            if lowered in {"from __future__ import annotations", "__init__.py"}:
                continue
            if "/" in line and (line.endswith(".py") or line.endswith(".js") or line.endswith(".html")):
                continue
            if len(line) >= 4:
                return line[:max_len].rstrip() + ("..." if len(line) > max_len else "")
        return ""

    in_frontmatter = False
    for raw_line in head.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if line == "---":
            in_frontmatter = not in_frontmatter
            continue
        if in_frontmatter or line.startswith(("#", "<!--", "```", ">")):
            continue
        line = re.sub(r"[*_`]+", "", line)
        line = re.sub(r"\s+", " ", line).strip()
        if len(line) >= 4:
            return line[:max_len].rstrip() + ("..." if len(line) > max_len else "")
    return ""


def scan_notes() -> list[NoteRef]:
    notes: list[NoteRef] = []
    for path in _iter_source_files():
        rel = path.relative_to(REPO_ROOT)
        try:
            head = path.read_text(encoding="utf-8", errors="replace")[:2400]
        except OSError:
            head = ""
        phase = _extract_phase(rel)
        category, kind = _classify(rel, phase)
        notes.append(
            NoteRef(
                path=path,
                rel=rel,
                title=_extract_title(path, head, kind),
                category=category,
                kind=kind,
                phase=phase,
                topics=_extract_topics(rel, head),
                summary=_extract_summary(head, kind),
            )
        )
    notes.sort(key=lambda note: note.rel.as_posix().lower())
    _polish_package_titles(notes)
    _disambiguate_titles(notes)
    return notes


def _polish_package_titles(notes: list[NoteRef]) -> None:
    """Give every `__init__.py` a human label instead of the stem "__init__".

    Before: lists showed `__init__ - src/audit` (disambiguation suffix).
    After: `src/audit` — the label is the dotted package path, which is what a
    reader actually wants to see when scanning a file list.
    """
    for note in notes:
        if note.path.name != "__init__.py":
            continue
        parts = list(note.rel.parent.parts)
        if parts[:1] == ["nova_backend"]:
            parts = parts[1:]
        if parts:
            note.title = "/".join(parts)


def _disambiguate_titles(notes: list[NoteRef]) -> None:
    by_title: dict[str, list[NoteRef]] = {}
    for note in notes:
        by_title.setdefault(note.title.lower(), []).append(note)
    for bucket in by_title.values():
        if len(bucket) < 2:
            continue
        for note in bucket:
            parent = note.rel.parent.as_posix() or "."
            hint = "/".join(parent.split("/")[-2:]) if parent != "." else parent
            note.title = f"{note.title} - {hint}"


def _wikilink(note: NoteRef) -> str:
    rel = note.rel.as_posix()
    if note.kind == "doc" and rel.lower().endswith(".md"):
        rel = rel[:-3]
    return f"[[{rel}|{note.title}]]"


def _link_target_exists(target: str) -> bool:
    return (REPO_ROOT / target).exists() or (REPO_ROOT / f"{target}.md").exists()


def _append_if_exists(lines: list[str], label: str, target: str) -> None:
    if _link_target_exists(target):
        lines.append(f"- [[{target}|{label}]]")


def _write(path: Path, body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(body, encoding="utf-8")


def _frontmatter(tags: list[str]) -> list[str]:
    return ["---", "tags:", *[f"  - {tag}" for tag in tags], "---"]


